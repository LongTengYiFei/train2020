//hlist discriminate head and node
struct hlist_head {
	struct hlist_node *first;
};

struct hlist_node {
	struct hlist_node *next, **pprev;
};

int hlist_unhashed(const struct hlist_node *h);
void hlist_add_head(struct hlist_node *n, struct hlist_head *h);
